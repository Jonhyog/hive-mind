const GraphsWorkspace = (): JSX.Element => {
  const radialHookData = useGetRealtime("/data");
  const setIncoming = useSetRealtime("/data/incoming");
  const setDeparting = useSetRealtime("/data/departing");

  const temperatureHookData = useGetRealtime("/lBBDn8");

  const temperatureData = useGetTemperature("7ae80c", "2ca599");
  const pressureData = useGetPressure("7ae80c", "d916f5");
  const humidityData = useGetHumidity("7ae80c", "b43223");

  const [sortedTemperatureData, setSortedTemperatureData] = useState([{}]);

  const processedTemperature = useMemo(() => {
    return temperatureData.map(({timestamp, value}) => {return {date: timestamp, temperature: value}});
  }, [temperatureData]);

  const processedPressure = useMemo(() => {
    return pressureData.map(({timestamp, value}) => {return {date: timestamp, pressure: value}});
  }, [pressureData]);

  const processedHumidity = useMemo(() => {
    return humidityData.map(({timestamp, value}) => {return {date: timestamp, humidity: value}})
  }, [humidityData]);

  useEffect(() => {
    console.log("Processed temperature data: ", processedTemperature);
  }, [processedTemperature]);

  useEffect(() => {
    console.log("Processed pressure data: ", processedPressure);
  }, [processedPressure]);

  useEffect(() => {
    console.log("Processed humidity data: ", processedHumidity);
  }, [processedHumidity]);


  useEffect(() => {
    // const boardRef = ref(db, "/lBBDn8");
    const lineData = Object.values(temperatureHookData).map(obj => {
      return {
        date: new Date(obj.timestamp).toISOString(),
        temperature: obj.temperature,
      }
    });

    lineData.sort(({ date: a}, {date: b}): number => {
      const aDate = new Date(a);
      const bDate = new Date(b);

      return aDate.getTime() - bDate.getTime();
    });

    setSortedTemperatureData(lineData);
  }, [temperatureHookData]);

  const handleIncomingUpdate = useCallback(() => {
    const randomNumber = Math.floor(Math.random() * 1000);
    setIncoming(randomNumber);
  }, [setIncoming]);

  const handleDepartingUpdate = useCallback(() => {
    const randomNumber = Math.floor(Math.random() * 1000);
    setDeparting(randomNumber);
  }, [setDeparting]);

  return (
    <div className="flex flex-col gap-4">
      <div className="flex flex-row flex-1 gap-4">
        <CustomBarChart
          chartData={barData}
          chartConfig={barConfig}
          className="w-2/3"
        />
        <div className="flex flex-1 flex-col gap-2">
          <Card className="flex-1">
            <CardHeader>
              <CardTitle>Update Firebase incoming bees</CardTitle>
              <CardDescription>
                Click the button to update Firebase Realtime Database incoming bees with random data.
              </CardDescription>
              <Button onClick={handleIncomingUpdate}>Generate Incoming</Button>
            </CardHeader>
          </Card>
          <Card className="flex-1">
            <CardHeader>
              <CardTitle>Update Firebase departing bees</CardTitle>
              <CardDescription>
              Click the button to update Firebase Realtime Database incoming bees with random data.
              </CardDescription>
              <Button onClick={handleDepartingUpdate}>Generate Departing</Button>
            </CardHeader>
          </Card>
        </div>
      </div>
      <div className="flex flex-row flex-1 gap-4">
        <CustomRadialChart
          chartData={radialHookData}
          chartConfig={radialConfig}
          className="w-1/3"
        />
        <LineChart
          chartData={processedTemperature}
          chartConfig={temperatureConfig}
          className="flex-1"
        />
        <LineChart
          chartData={processedPressure}
          chartConfig={pressureConfig}
          className="flex-1"
        />
        <LineChart
          chartData={processedHumidity}
          chartConfig={humidityConfig}
          className="flex-1"
        />
        {/* <LineMock className="flex-1" /> */}
      </div>
    </div>
  );
};

